    private void randomButtonColor() {
        int[] array = {1, 2, 3, 4, 5};
        int index;
        Random random = new Random();
        for (int i = array.length - 1; i > 0; i--) {
            index = random.nextInt(i + 1);
            if (index != i) {
                array[index] ^= array[i];
                array[i] ^= array[index];
                array[index] ^= array[i];
            }
        }
        chooseColor(array[0], jButton27);
        chooseColor(array[1], jButton28);
        chooseColor(array[2], jButton29);
        chooseColor(array[3], jButton30);
        chooseColor(array[4], jButton31);
    }

