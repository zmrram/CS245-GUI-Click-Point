    private void checkSudokuAnswer() {
        int error = 0;
        for (int i = 0; i < sudokuKey[0].length; i++) {
            for (int j = 0; j < sudokuKey[0].length; j++) {
                if (sudokuGrid[i][j] != sudokuKey[i][j]) {
                    sudokuScore = sudokuScore - sudoScore[i][j];
                    sudoScore[i][j] = 0;
                    error++;
                }
            }
        }
        jLabel33.setText(Integer.toString(totalScore + sudokuScore));
        if (error > 0) {
            JOptionPane.showMessageDialog(null, "Sorry, your answer was incorrect", "WRONG!", JOptionPane.WARNING_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "You've submitted a correct answer", "CORRECT!!!", JOptionPane.WARNING_MESSAGE);
            totalScore = totalScore + sudokuScore;
            jLabel25.setText(Integer.toString(totalScore));
            CardLayout card = (CardLayout) menuPanel.getLayout();
            card.show(menuPanel, "GameOver");
            checkHighScore();
        }

    }

