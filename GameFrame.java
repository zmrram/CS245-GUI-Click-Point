    /**
     * method: checkAnswer purpose: every time the user choose a letter in the
     * Hangman game, this method will invoke. First the button will be disable
     * immidiately after clicked then this method will check if the letter
     * matched with any character of the secretWord and it's position then
     * display that letter.
     *
     */
    private void checkAnswer(javax.swing.JButton b) {
        int matchLetters = 0;
        char[] strChars = secretLabelString.toCharArray();
        for (int i = 0; i < secretWord.length(); i++) {
            if (secretWord.charAt(i) == b.getActionCommand().charAt(0)) {
                strChars[i] = b.getActionCommand().charAt(0);
                matchLetters++;
            }
        }
        if (matchLetters == 0 && totalScore >= 40 && b.isEnabled() == true) {
            totalScore -= 10;
            jLabel22.setText(Integer.toString(totalScore));
            switch (totalScore) {
                case 90:
                    head.setVisible(true);
                    break;
                case 80:
                    body.setVisible(true);
                    break;
                case 70:
                    leftArm.setVisible(true);
                    break;
                case 60:
                    rightArm.setVisible(true);
                    break;
                case 50:
                    leftLeg.setVisible(true);
                    break;
                case 40:
                    rightLeg.setVisible(true);
                    break;

            }
        }
        b.setEnabled(false);
        if (totalScore == 40) {
            CardLayout card = (CardLayout) menuPanel.getLayout();
            card.show(menuPanel, "ColorGame");
            initializeColorGame();

        }
        secretLabelString = String.valueOf(strChars);
        secretLabel.setText(secretLabelString);
    }

    /**
     * method: checkGameStatus purpose: this method would check whether the game
     * is coming to an end or not.
     *
     */
    private void checkGameStatus() {
        int x = 0;
        for (int i = 0; i < secretLabelString.length(); i++) {
            if (secretLabelString.charAt(i) == '-') {
                x++;
            }
        }
        if (x == 0) {
            CardLayout card = (CardLayout) menuPanel.getLayout();
            card.show(menuPanel, "ColorGame");
            initializeColorGame();
        }
    }

