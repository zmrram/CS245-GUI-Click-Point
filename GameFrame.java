    private void checkHighScore() {
        int[] scoreArray = null;
        String[] nameArray = null;
        try// If this doesnt work throw an exception
        {
            FileInputStream fileIn = new FileInputStream("score.sav");// Read serial file.
            FileInputStream fileIn2 = new FileInputStream("name.sav");
            ObjectInputStream in = new ObjectInputStream(fileIn);// input the read file.
            ObjectInputStream in2 = new ObjectInputStream(fileIn2);
            scoreArray = (int[]) in.readObject();// allocate it to the object file already instanciated.
            nameArray = (String[]) in2.readObject();
            in.close();//closes the input stream.
            in2.close();
            fileIn.close();//closes the file data stream.
            fileIn2.close();
        } catch (IOException i) {
            i.printStackTrace();
        } catch (ClassNotFoundException c) {

            c.printStackTrace();
        }
        String name = "ABC";
        for (int i = 0; i < scoreArray.length; i++) {
            if (totalScore > scoreArray[i]) {
                while (name.length() > 2) {
                    name = JOptionPane.showInputDialog("You've set a HIGH SCORE!!!", "Please enter 2 letter initials");
                    if (name == null) {
                        name = "N.N";
                        break;
                    }

                }
                for (int j = 4; j > i; j--) {
                    scoreArray[j] = scoreArray[j - 1];
                    nameArray[j] = nameArray[j - 1];

                }
                scoreArray[i] = totalScore;
                nameArray[i] = name;

                try {
                    FileOutputStream fileOut = new FileOutputStream("score.sav");//creates a card serial file in output stream
                    FileOutputStream fileOut2 = new FileOutputStream("name.sav");
                    ObjectOutputStream out = new ObjectOutputStream(fileOut);//routs an object into the output stream.
                    ObjectOutputStream out2 = new ObjectOutputStream(fileOut2);
                    out.writeObject(scoreArray);// we designate our array of cards to be routed
                    out2.writeObject(nameArray);
                    out.close();// closes the data paths
                    out2.close();
                    fileOut.close();// closes the data paths
                    fileOut2.close();
                } catch (IOException e)//exception stuff
                {
                    e.printStackTrace();
                }
                break;
            }
        }
    }

