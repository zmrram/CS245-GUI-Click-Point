    /**
     * method: getDateAndTime purpose: this method will get the date and time
     * which is imported from the Calendar package, and also use the Timer to
     * update the time every 1 second
     *
     */
    private void getDateAndTime() {

        timer = new Timer(1000, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                Calendar cal = new GregorianCalendar();
                int month = cal.get(Calendar.MONTH) + 1;
                int date = cal.get(Calendar.DATE);
                int year = cal.get(Calendar.YEAR);
                int hour = cal.get(Calendar.HOUR);
                int minute = cal.get(Calendar.MINUTE);
                int second = cal.get(Calendar.SECOND);
                jLabel26.setText(month + "/" + date + "/" + year + " - " + hour + ":" + minute + ":" + second);
                jLabel28.setText(month + "/" + date + "/" + year + " - " + hour + ":" + minute + ":" + second);
                jLabel35.setText(month + "/" + date + "/" + year + " - " + hour + ":" + minute + ":" + second);
            }
        });
        timer.start();
    }

